package usearch.entity;

public class RssToDistance {
	float rssiStart;
	float rssiEnd;
	String distanceStart;
	String distanceEnd;
	String charset;
	public float getRssiStart() {
		return rssiStart;
	}
	public void setRssiStart(float rssiStart) {
		this.rssiStart = rssiStart;
	}
	public float getRssiEnd() {
		return rssiEnd;
	}
	public void setRssiEnd(float rssiEnd) {
		this.rssiEnd = rssiEnd;
	}
	public String getDistanceStart() {
		return distanceStart;
	}
	public void setDistanceStart(String distanceStart) {
		this.distanceStart = distanceStart;
	}
	public String getDistanceEnd() {
		return distanceEnd;
	}
	public void setDistanceEnd(String distanceEnd) {
		this.distanceEnd = distanceEnd;
	}
	public String getCharset() {
		return charset;
	}
	public void setCharset(String charset) {
		this.charset = charset;
	}
	
	
}
